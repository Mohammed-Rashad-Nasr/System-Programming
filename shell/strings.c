//********** function to count number of semicolons in given string line  **********
//**********************************************************************************
int semicount(char *line)
{
    int iterator = 0;
    int count = 0;
    while (line[iterator] != '\0') 
    {
	if (line[iterator] == ';')
	    count++;
	
        iterator++;
    }

    return count;
}
