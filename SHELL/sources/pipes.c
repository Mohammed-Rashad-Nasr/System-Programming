//********** function to check if given string has piping or not using "|"***********
//***********************************************************************************

int ispipe(char *line)
{
    int iterator = 0;
    while (line[iterator] != '\0') 
    {

	if (line[iterator] == '|')
	    return 1;               //if there is piping return one
	
        iterator++;
    }
    return 0;                       //if there is no piping return zero
}

